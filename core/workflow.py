from __future__ import annotations

import copy
import json
import struct
import zlib
from pathlib import Path
from typing import Any

from core.exceptions import ComfyUIError


_PNG_SIGNATURE = b'\x89PNG\r\n\x1a\n'


def extract_workflow_from_png(path: str) -> dict:
    """Extract a ComfyUI API-format workflow dict from a PNG file.

    Reads ``tEXt`` and ``iTXt`` metadata chunks, trying the ``"workflow"``
    key first (written by ComfyWeave / ComfyUI API output) then ``"prompt"``
    (written by ComfyUI's standard UI save).  Raises ``ValueError`` if no
    usable workflow is found.
    """
    file_path = Path(path)
    if not file_path.exists():
        raise FileNotFoundError(f"File not found: {path}")

    with file_path.open("rb") as fh:
        sig = fh.read(8)
        if sig != _PNG_SIGNATURE:
            raise ValueError("Not a valid PNG file.")

        text_chunks: dict[str, str] = {}
        while True:
            hdr = fh.read(8)  # 4-byte length + 4-byte type
            if len(hdr) < 8:
                break
            length = struct.unpack(">I", hdr[:4])[0]
            chunk_type = hdr[4:].decode("ascii", errors="replace")
            data = fh.read(length)
            fh.read(4)  # CRC (skip)

            if chunk_type == "tEXt":
                try:
                    null = data.index(b"\x00")
                    keyword = data[:null].decode("latin-1")
                    text = data[null + 1:].decode("latin-1")
                    text_chunks[keyword] = text
                except (ValueError, UnicodeDecodeError):
                    pass

            elif chunk_type == "iTXt":
                try:
                    null = data.index(b"\x00")
                    keyword = data[:null].decode("latin-1")
                    rest = data[null + 1:]
                    comp_flag = rest[0]
                    rest = rest[2:]  # skip comp_flag + comp_method
                    rest = rest[rest.index(b"\x00") + 1:]  # skip language tag
                    rest = rest[rest.index(b"\x00") + 1:]  # skip translated keyword
                    text_bytes = zlib.decompress(rest) if comp_flag else rest
                    text_chunks[keyword] = text_bytes.decode("utf-8")
                except (ValueError, zlib.error, UnicodeDecodeError):
                    pass

            if chunk_type == "IEND":
                break

    for key in ("workflow", "prompt"):
        raw_text = text_chunks.get(key)
        if raw_text is None:
            continue
        try:
            return json.loads(raw_text)
        except json.JSONDecodeError:
            continue

    raise ValueError(
        "No ComfyUI workflow metadata found in this PNG.\n\n"
        "The image must have been generated by ComfyUI with 'Save Image' "
        "or by ComfyWeave."
    )


